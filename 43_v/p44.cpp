#include <iostream>

using namespace std;

/*
    #44
    Problem:
    Write a program to ask the user to enter:

    • Day

    Then print the day as follows:
    • 1 → Print Sunday
    • 2 → Print Monday
    • 3 → Print Tuesday
    • 4 → Print Wednesday
    • 5 → Print Thursday
    • 6 → Print Friday
    • 7 → Print Saturday
    • Otherwise → Print “Wrong Day” and ask the user to enter the day again

    Example:

    Inputs:
    6

    Outputs:
    Its Friday
*/

int main()
{
    int Day;

    cout << "Enter Day (1-7): ";
    cin >> Day;

    if (Day == 1)
    {
        cout << "Its Sunday" << endl;
    }
    
    else if (Day == 2)
    {
        cout << "Its Monday" << endl;
    }
    
    else if (Day == 3)
    {
        cout << "Its Tuesday" << endl;
    }
    
    else if (Day == 4)
    {
        cout << "Its Wednesday" << endl;
    }
    
    else if (Day == 5)
    {
        cout << "Its Thursday" << endl;
    }
    
    else if (Day == 6)
    {
        cout << "Its Friday" << endl;
    }
    
    else if (Day == 7)
    {
        cout << "Its Saturday" << endl;
    }
    
    else
    {
        cout << "Wrong Day, please enter a valid day (1-7)." << endl;
    }

    return 0;
}
